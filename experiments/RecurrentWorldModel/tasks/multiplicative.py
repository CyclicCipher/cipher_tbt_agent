"""MultiplicativeRing -- the multiplication generality test (BLUEPRINT §G #2; PURE_MATH §2-3).

Multiplication mod a prime p, restricted to the nonzero residues, is the **cyclic group of order p-1**
generated by a primitive root g: every nonzero residue is g^i, and a*b = g^(log_g a + log_g b). So on
the **log/exponent frame** multiplication IS addition. This class feeds the *same* discovery machinery
(`train_numberline.train_discovery`) the multiplicative successor `x -> x*g` instead of `+1`, testing
whether the same mechanism discovers a DIFFERENT frame (the log scale, values ordered by exponent) for a
different operation -- and whether `x` then falls out zero-shot via path integration `R^(log_g b) z(a)`.

Differences from the additive number line: values are presented in **orbit order** g^0, g^1, ... (not
1,2,3,...); the group order is p-1 (0 is excluded -- absorbing, no inverse); the metric `dist` is the
circular distance on the **exponent** ring. Everything else (rotation equivariance, closure, SIGReg,
probes) is inherited unchanged -- that reuse is the point.
"""

from __future__ import annotations

from . import vocab
from .number_line import NumberLineDiscovery


class MultiplicativeRing(NumberLineDiscovery):
    def __init__(self, p: int = 17, g: int = 3, seed: int = 0, value_coord: bool = False) -> None:
        self.p_mod = p
        self.g = g
        self.ginv = pow(g, -1, p)
        self.P = p - 1                                       # group order (nonzero residues)
        self.vocab_size = vocab.vocab_size(p)                # VAL tokens cover residues 0..p-1
        self.values = [pow(g, i, p) for i in range(self.P)]  # orbit order: g^0, g^1, ... = the log frame
        self.index = {v: i for i, v in enumerate(self.values)}   # discrete log: value -> exponent
        self._seed = seed
        self.value_coord = value_coord
        self.all_compare = [(a, b) for a in self.values for b in self.values]

    # the generator's step is *g (and its inverse), in place of +1 / -1
    def succ(self, a: int) -> int:
        return (a * self.g) % self.p_mod

    def pred(self, a: int) -> int:
        return (a * self.ginv) % self.p_mod

    def dist(self, i: int, j: int) -> int:
        """Circular distance on the exponent ring. Args are orbit indices (the probes pass 0..P-1, which
        are exactly the exponents, since values are presented in orbit order)."""
        d = (i - j) % self.P
        return min(d, self.P - d)
