"""
Phase 8 Integration Test — Multi-Anomaly Coverage Abduction (A-6).

Chains Phases 1–8 end-to-end.

Scenario A — Shared Generating Process
---------------------------------------
Four anomaly sets are generated by two different processes:
  Process P:  f(x) = 6 * x   (3 anomaly sets)
  Process Q:  f(x) = 10 * x  (1 anomaly set)

multi_anomaly_abduction must:
  1. Find the hypothesis H that covers the most anomaly sets.
  2. H covers ≥ 3 of the 4 sets (the majority = P).
  3. H is stored as a COVERAGE_ABDUCTION morphism in the graph.
  4. query_coverage_abductions returns the stored report.

Scenario B — MDL tiebreak with equal coverage
-----------------------------------------------
All 4 anomaly sets generated by P = 6*x.
Two schema candidates: 1-param and 3-param.
Coverage is tied (both explain all 4 sets).
The 1-param candidate wins on MDL.

Scenario C — Full pipeline
----------------------------
Phase 4 Newton theory + Phase 6 RetractEngine + Phase 7 latent +
Phase 8 multi-anomaly abduction.
Three anomaly streams recorded against a Newton morphism (k=6);
abduction recovers the shared 6*x process.

Test classes
------------
TestPhase8Integration (8 tests)
    - multi_anomaly_abduction returns non-None for mixed sets
    - coverage ≥ 0.75 (majority correctly identified)
    - query returns stored report
    - report coverage field matches score
    - MDL tiebreak selects simpler model
    - correct anomaly sets explained
    - held-out prediction from winner
    - integration: Phase 4 theory + Phase 8 abduction

TestPhase8Cage (3 tests)
    - 10 seeds: majority process found
    - 10 seeds: coverage ≥ 0.5
    - 5 seeds: query returns reports

TestPhase8DefectProbe (4 tests)
    Probe 1: coverage is primary key, not MDL
    Probe 2: minority process is NOT the winner
    Probe 3: two symbol tables produce same coverage fraction
    Probe 4: wrong-scale hypothesis gets zero coverage on correct sets
"""
