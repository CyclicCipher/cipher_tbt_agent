class TestPhase8DefectProbe:

    def test_probe1_coverage_is_primary_key(self):
        """Coverage beats MDL as primary selection key."""
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        # Three sets from 6*x (majority), one from 10*x
        sets = [_obs_set(6.0), _obs_set(6.0), _obs_set(6.0), _obs_set(10.0)]
        hyp = multi_anomaly_abduction(
            sets, [_schema_g(nid)], _schema_h(nid), ctx, mg, tm, t,
            tolerance=0.05,
        )
        assert hyp is not None
        cov = score_coverage(hyp, sets, ctx, tolerance=0.05)
        # Winning hypothesis must cover the majority (≥3 sets), even if there
        # were a minority hypothesis with lower MDL
        assert cov.n_explained >= 3, \
            "PROBE 1: primary selection key must be coverage, not MDL"

    def test_probe2_minority_not_winner(self):
        """The 10*x anomaly set must NOT be the explanation for the 6*x sets."""
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        # 3 sets from 6*x, 1 from 10*x
        sets = [_obs_set(6.0), _obs_set(6.0), _obs_set(6.0), _obs_set(10.0)]
        hyp = multi_anomaly_abduction(
            sets, [_schema_g(nid)], _schema_h(nid), ctx, mg, tm, t,
            tolerance=0.05,
        )
        assert hyp is not None
        cov = score_coverage(hyp, sets, ctx, tolerance=0.05)
        # If minority won, it would only explain 1 set
        assert cov.n_explained >= 3, \
            f"PROBE 2: minority process won; only {cov.n_explained}/4 explained"

    def test_probe3_two_tables_same_coverage(self):
        """Two symbol tables produce the same coverage fraction."""
        fracs = []
        for seed in range(2):
            ctx, nid = _anon_ctx(seed)
            mg = MorphismGraph()
            tm = TheoryManager(mg)
            t = tm.register_theory("T")
            sets = [_obs_set(6.0), _obs_set(6.0), _obs_set(10.0)]
            hyp = multi_anomaly_abduction(
                sets, [_schema_g(nid)], _schema_h(nid), ctx, mg, tm, t,
                tolerance=0.05, label_prefix=f"p8d3_{seed}",
            )
            assert hyp is not None, f"seed {seed}: None"
            cov = score_coverage(hyp, sets, ctx, tolerance=0.05)
            fracs.append(cov.coverage)
        assert fracs[0] == pytest.approx(fracs[1], abs=0.01), \
            f"PROBE 3: coverage fractions differ: {fracs[0]} vs {fracs[1]}"

    def test_probe4_wrong_scale_zero_coverage(self):
        """A hypothesis fit on 100*x explains none of the 6*x anomaly sets."""
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        # Fit specifically on 100*x data
        hyp_wrong = hypothesise_latent(
            _obs_set(100.0), _schema_g(nid), _schema_h(nid),
            ctx, mg, tm, t, label_prefix="p8d4_wrong",
        )
        assert hyp_wrong is not None
        # Score against 6*x anomaly sets
        sets = [_obs_set(6.0), _obs_set(6.0), _obs_set(6.0)]
        cov = score_coverage(hyp_wrong, sets, ctx, tolerance=0.05)
        assert cov.n_explained == 0, \
            f"PROBE 4: wrong-scale hypothesis explains {cov.n_explained} sets (expected 0)"
