class TestPhase8Integration:

    def test_multi_abduction_non_none(self):
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        sets = [_obs_set(6.0), _obs_set(6.0), _obs_set(6.0), _obs_set(10.0)]
        hyp = multi_anomaly_abduction(
            sets, [_schema_g(nid)], _schema_h(nid), ctx, mg, tm, t,
            tolerance=0.05,
        )
        assert hyp is not None

    def test_majority_coverage(self):
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        sets = [_obs_set(6.0), _obs_set(6.0), _obs_set(6.0), _obs_set(10.0)]
        hyp = multi_anomaly_abduction(
            sets, [_schema_g(nid)], _schema_h(nid), ctx, mg, tm, t,
            tolerance=0.05,
        )
        assert hyp is not None
        cov = score_coverage(hyp, sets, ctx, tolerance=0.05)
        assert cov.coverage >= 0.75

    def test_query_returns_report(self):
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        sets = [_obs_set(6.0), _obs_set(6.0)]
        multi_anomaly_abduction(
            sets, [_schema_g(nid)], _schema_h(nid), ctx, mg, tm, t,
            label_prefix="p8_query",
        )
        reports = query_coverage_abductions(mg, t)
        assert len(reports) >= 1

    def test_report_coverage_field(self):
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        sets = [_obs_set(6.0), _obs_set(6.0), _obs_set(6.0)]
        hyp = multi_anomaly_abduction(
            sets, [_schema_g(nid)], _schema_h(nid), ctx, mg, tm, t,
            tolerance=0.05,
        )
        assert hyp is not None
        reports = query_coverage_abductions(mg, t)
        assert len(reports) >= 1
        # The stored report's coverage should be > 0
        assert reports[0].coverage.coverage > 0.0

    def test_mdl_tiebreak_simpler_wins(self):
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        # All sets from 6*x — both schemas cover all; MDL selects 1-param
        sets = [_obs_set(6.0)] * 4
        hyp = multi_anomaly_abduction(
            sets, [_schema_g(nid), _schema_g3(nid)], _schema_h(nid),
            ctx, mg, tm, t, tolerance=0.05,
        )
        assert hyp is not None
        # Simpler hypothesis has fewer params (1-param schema_g)
        n_params = len(hyp.input_law.schema.params) + len(hyp.output_law.schema.params)
        # The 1-param schema_g has n_params_g=1; 3-param has n_params_g=3
        # (schema_h has 1 param regardless)
        assert n_params <= 2  # 1-param g + 1-param h = 2

    def test_correct_sets_explained(self):
        """The three 6*x anomaly sets must all be explained by the winner."""
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        sets = [_obs_set(6.0), _obs_set(6.0), _obs_set(6.0), _obs_set(10.0)]
        hyp = multi_anomaly_abduction(
            sets, [_schema_g(nid)], _schema_h(nid), ctx, mg, tm, t,
            tolerance=0.05,
        )
        assert hyp is not None
        cov = score_coverage(hyp, sets, ctx, tolerance=0.05)
        assert cov.n_explained >= 3

    def test_holdout_prediction(self):
        """Winner hypothesis predicts held-out observations correctly."""
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        sets = [_obs_set(6.0, n=5), _obs_set(6.0, n=5), _obs_set(10.0, n=5)]
        hyp = multi_anomaly_abduction(
            sets, [_schema_g(nid)], _schema_h(nid), ctx, mg, tm, t,
            tolerance=0.05,
        )
        assert hyp is not None
        # Held-out: x=10 should predict ≈ 6*10 = 60 (from the 6*x process)
        z    = predict_continuous(hyp.input_law,  {"x": 10.0}, ctx)
        pred = predict_continuous(hyp.output_law, {"z": z},    ctx)
        cov = score_coverage(hyp, sets, ctx, tolerance=0.05)
        if cov.coverage >= 0.5:   # majority was 6*x
            assert pred == pytest.approx(60.0, rel=0.10)

    def test_full_pipeline_with_theory(self):
        """Phase 4 theory + Phase 7 latent + Phase 8 multi-anomaly abduction."""
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("Newton")

        # Phase 4: add a known wrong morphism
        mid = add_fitted_law(mg, "wrong_k", _fitted_law(nid, 5.0))
        tm.assign_morphism(mid, t)

        # Three anomaly sets from the true process (6*x) + one outlier (20*x)
        sets = [_obs_set(6.0, n=4), _obs_set(6.0, n=6), _obs_set(6.0, n=8),
                _obs_set(20.0, n=4)]

        hyp = multi_anomaly_abduction(
            sets, [_schema_g(nid)], _schema_h(nid), ctx, mg, tm, t,
            tolerance=0.05, label_prefix="newton_multi",
        )
        assert hyp is not None
        cov = score_coverage(hyp, sets, ctx, tolerance=0.05)
        assert cov.n_explained >= 3


