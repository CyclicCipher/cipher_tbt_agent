class TestSelectBest:

    def test_empty_list_returns_none(self):
        ctx, nid = _ctx()
        best = select_best_covering_hypothesis([], [_obs_set(6.0)], ctx)
        assert best is None

    def test_single_hypothesis_returned(self):
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        hyp = _make_hyp(nid, 6.0, ctx, mg, tm, t)
        assert hyp is not None
        best = select_best_covering_hypothesis([hyp], [_obs_set(6.0)], ctx)
        assert best is hyp

    def test_best_coverage_wins(self):
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        # hyp6 covers 3 sets, hyp10 covers 1
        hyp6  = _make_hyp(nid, 6.0,  ctx, mg, tm, t)
        hyp10 = _make_hyp(nid, 10.0, ctx, mg, tm, t)
        assert hyp6 is not None and hyp10 is not None
        sets = [_obs_set(6.0), _obs_set(6.0), _obs_set(6.0), _obs_set(10.0)]
        best = select_best_covering_hypothesis([hyp6, hyp10], sets, ctx, tolerance=0.05)
        # hyp6 explains 3/4; hyp10 explains 1/4 → hyp6 wins
        assert best is hyp6

    def test_mdl_tiebreak(self):
        """When coverage is tied, lower MDL wins."""
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        # Both hypotheses fit 6*x equally well
        hyp_a = _make_hyp(nid, 6.0, ctx, mg, tm, t)
        hyp_b = _make_hyp(nid, 6.0, ctx, mg, tm, t)
        assert hyp_a is not None and hyp_b is not None
        # Artificially inflate hyp_b's mdl_score
        from dataclasses import replace
        hyp_b_worse = LatentHypothesis(
            latent_id=hyp_b.latent_id,
            input_law=hyp_b.input_law,
            output_law=hyp_b.output_law,
            residual=hyp_b.residual,
            mdl_score=hyp_b.mdl_score + 1000.0,
            morph_id=hyp_b.morph_id,
        )
        sets = [_obs_set(6.0), _obs_set(6.0)]
        best = select_best_covering_hypothesis([hyp_a, hyp_b_worse], sets, ctx, tolerance=0.05)
        assert best is hyp_a


