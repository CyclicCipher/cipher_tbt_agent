    def test_multiple_schema_candidates_selects_best(self):
        """With two schema candidates, the one that explains more anomalies wins."""
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
