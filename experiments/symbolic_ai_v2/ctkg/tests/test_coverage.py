class TestDefectProbe:

    def test_probe1_shared_not_individual(self):
        """multi_anomaly_abduction returns ONE hypothesis (shared explanation).
        A defective system would return K separate hypotheses (one per anomaly).
        We can't easily detect K returns, but we CAN check that the returned
        hypothesis covers multiple anomaly sets, not just one."""
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        sets = [_obs_set(6.0), _obs_set(6.0), _obs_set(6.0)]
        hyp = multi_anomaly_abduction(
            sets, [_schema_g(nid)], _schema_h(nid), ctx, mg, tm, t,
            tolerance=0.05,
        )
        assert hyp is not None
        cov = score_coverage(hyp, sets, ctx, tolerance=0.05)
        # A shared explanation covers ALL sets, not just 1
        assert cov.n_explained == 3, \
            f"PROBE 1: shared explanation should cover all 3 sets, got {cov.n_explained}"

    def test_probe2_coverage_not_mdl_primary(self):
        """select_best uses coverage as primary key, not MDL.
        A defective system using MDL as primary key would fail this."""
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        # hyp_a covers 2 sets but has higher MDL
        # hyp_b covers 1 set but has lower MDL
        hyp_a = _make_hyp(nid, 6.0, ctx, mg, tm, t)
        hyp_b = _make_hyp(nid, 10.0, ctx, mg, tm, t)
        assert hyp_a is not None and hyp_b is not None
        # Ensure hyp_b has lower MDL artificially
        hyp_b_low = LatentHypothesis(
            latent_id=hyp_b.latent_id,
            input_law=hyp_b.input_law,
            output_law=hyp_b.output_law,
            residual=hyp_b.residual,
            mdl_score=0.001,    # very low MDL
            morph_id=hyp_b.morph_id,
        )
        sets = [_obs_set(6.0), _obs_set(6.0), _obs_set(10.0)]
        best = select_best_covering_hypothesis(
            [hyp_a, hyp_b_low], sets, ctx, tolerance=0.05,
        )
        # hyp_a covers 2 sets (6*x observations); hyp_b_low covers 1 (10*x)
        # Coverage primary key → hyp_a should win despite higher MDL
        cov_a = score_coverage(hyp_a,     sets, ctx, tolerance=0.05)
        cov_b = score_coverage(hyp_b_low, sets, ctx, tolerance=0.05)
        if cov_a.n_explained > cov_b.n_explained:
            assert best is hyp_a, \
                "PROBE 2: coverage must be the primary ranking key, not MDL"

    def test_probe3_wrong_data_zero_coverage(self):
        """A hypothesis fit on wrong data explains zero anomalies from correct data."""
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        # Fit on 100*x (very different scale)
        hyp_wrong = _make_hyp(nid, 100.0, ctx, mg, tm, t)
        assert hyp_wrong is not None
        # Score on 6*x anomalies
        sets = [_obs_set(6.0), _obs_set(6.0), _obs_set(6.0)]
        cov = score_coverage(hyp_wrong, sets, ctx, tolerance=0.05)
        assert cov.n_explained == 0, \
            f"PROBE 3: wrong-data hypothesis should explain 0 sets, got {cov.n_explained}"

    def test_probe4_two_tables_same_coverage(self):
        """Two symbol tables produce the same coverage fraction."""
        fractions = []
        for seed in range(2):
            ctx, nid = _anon_ctx(seed)
            mg = MorphismGraph()
            tm = TheoryManager(mg)
            t = tm.register_theory("T")
            sets = [_obs_set(6.0), _obs_set(6.0), _obs_set(10.0)]
            hyp = multi_anomaly_abduction(
                sets, [_schema_g(nid)], _schema_h(nid), ctx, mg, tm, t,
                tolerance=0.05, label_prefix=f"p8d4_{seed}",
            )
            assert hyp is not None, f"seed {seed}: None"
            cov = score_coverage(hyp, sets, ctx, tolerance=0.05)
            fractions.append(cov.coverage)
        assert fractions[0] == pytest.approx(fractions[1], abs=0.01), \
            f"PROBE 4: coverage fractions differ: {fractions[0]} vs {fractions[1]}"
