class TestCoverageScoring:

    def test_perfect_coverage(self):
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        hyp = _make_hyp(nid, 6.0, ctx, mg, tm, t)
        assert hyp is not None
        # All 3 sets generated by 6*x → hypothesis should explain all
        sets = [_obs_set(6.0), _obs_set(6.0, n=3), _obs_set(6.0, n=5)]
        cov = score_coverage(hyp, sets, ctx, tolerance=0.05)
        assert cov.n_explained == 3
        assert cov.n_total == 3
        assert cov.coverage == pytest.approx(1.0)

    def test_partial_coverage(self):
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        # Fit on 6*x
        hyp = _make_hyp(nid, 6.0, ctx, mg, tm, t)
        assert hyp is not None
        # 2 sets from 6*x, 2 sets from 10*x (very different)
        sets = [_obs_set(6.0), _obs_set(6.0), _obs_set(10.0), _obs_set(10.0)]
        cov = score_coverage(hyp, sets, ctx, tolerance=0.05)
        assert cov.n_explained == 2
        assert cov.coverage == pytest.approx(0.5)

    def test_zero_coverage(self):
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        # Fit on 6*x but test on 100*x
        hyp = _make_hyp(nid, 6.0, ctx, mg, tm, t)
        assert hyp is not None
        sets = [_obs_set(100.0), _obs_set(200.0)]
        cov = score_coverage(hyp, sets, ctx, tolerance=0.05)
        assert cov.n_explained == 0
        assert cov.coverage == pytest.approx(0.0)

    def test_empty_anomaly_sets(self):
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        hyp = _make_hyp(nid, 6.0, ctx, mg, tm, t)
        assert hyp is not None
        cov = score_coverage(hyp, [], ctx)
        assert cov.n_explained == 0
        assert cov.n_total == 0
        assert cov.coverage == pytest.approx(0.0)

    def test_single_observation_per_set(self):
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        hyp = _make_hyp(nid, 6.0, ctx, mg, tm, t)
        assert hyp is not None
        sets = [[({  "x": 3.0}, 18.0)], [({  "x": 5.0}, 30.0)]]
        cov = score_coverage(hyp, sets, ctx, tolerance=0.05)
        assert cov.n_explained == 2

    def test_tolerance_boundary(self):
        """An anomaly set just below tolerance should be explained; just above should not."""
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        hyp = _make_hyp(nid, 6.0, ctx, mg, tm, t)
        assert hyp is not None
        # 6*x prediction should be ~exact → relative error ≈ 0 << 0.10
        cov_loose = score_coverage(hyp, [_obs_set(6.0)], ctx, tolerance=0.10)
        assert cov_loose.n_explained == 1

    def test_residuals_length_matches_n_total(self):
        ctx, nid = _ctx()
        mg = MorphismGraph()
        tm = TheoryManager(mg)
        t = tm.register_theory("T")
        hyp = _make_hyp(nid, 6.0, ctx, mg, tm, t)
        assert hyp is not None
        sets = [_obs_set(6.0), _obs_set(10.0), _obs_set(6.0)]
        cov = score_coverage(hyp, sets, ctx)
        assert len(cov.residuals_per_set) == 3


