"""
Spine: compression-tree position tracker for the CTKG prediction pipeline.

The Spine tracks which grammar rule body is currently being expanded and how
far along it is, allowing the Predictor to decide whether the next token should
be generated by a non-terminal expansion or by the process rule / Kan extension.

Usage during generation:
    spine = Spine()
    spine.push(rule_id=0, body=[...])        # push the start rule
    while not spine.is_empty():
        frame = spine.peek()
        sym = frame.body[frame.pos]          # next symbol to expand
        if _is_nonterminal(sym):
            spine.push(sym, grammar.rules[sym].body)  # descend
        else:
            # generate the terminal (the atom)
            spine.advance()
            if spine.peek() and spine.peek().is_done():
                spine.pop()  # rule body exhausted

Each SpineFrame stores a rule_id, the encoded body (from graph_grammar.py symbol
convention), and the current position within that body.

See CTKG_ARCHITECTURE.md §Phase 5 for the full specification.
"""

from __future__ import annotations

from dataclasses import dataclass
from typing import Optional


@dataclass
class SpineFrame:
    """One level in the grammar-rule expansion stack.

    Attributes
    ----------
    rule_id:
        The grammar rule being expanded (non-negative integer; 0 = start rule).
    body:
        The encoded symbol list for this rule (from graph_grammar.py encoding:
        negative = terminal concept_id, non-negative = non-terminal rule_id).
    pos:
        Current position in body (index of the next symbol to expand).
    """

    rule_id: int
    body: list[int]
    pos: int = 0

    def is_done(self) -> bool:
        """Return True if all symbols in this frame have been expanded."""
        return self.pos >= len(self.body)


class Spine:
    """Lightweight stack-based compression-tree position tracker.

    Each push corresponds to entering a new grammar rule body.
    advance() moves to the next symbol in the top frame.
    pop() removes the top frame when its body is exhausted.
    """

    def __init__(self) -> None:
        self._stack: list[SpineFrame] = []

    # ------------------------------------------------------------------
    # Stack operations
    # ------------------------------------------------------------------

    def push(self, rule_id: int, body: list[int]) -> None:
        """Push a new grammar rule frame onto the spine."""
        self._stack.append(SpineFrame(rule_id=rule_id, body=list(body), pos=0))

    def pop(self) -> Optional[SpineFrame]:
        """Remove and return the top frame, or None if the spine is empty."""
        if not self._stack:
            return None
        return self._stack.pop()

    def peek(self) -> Optional[SpineFrame]:
        """Return the top frame without removing it, or None if empty."""
        if not self._stack:
            return None
        return self._stack[-1]

    def advance(self) -> None:
        """Increment the position in the top frame by one.

        No-op if the spine is empty.
        """
        if self._stack:
            self._stack[-1].pos += 1

    # ------------------------------------------------------------------
    # Query
    # ------------------------------------------------------------------

    def is_empty(self) -> bool:
        """Return True if the spine has no frames."""
        return len(self._stack) == 0

    def depth(self) -> int:
        """Return the number of frames currently on the spine."""
        return len(self._stack)

    def open_nonterminals(self) -> list[int]:
        """Return rule_ids of all partially-expanded frames on the spine.

        An "open non-terminal" is a grammar rule that has been entered
        (pushed onto the spine) but whose body has not been fully expanded.
        Returns rule IDs from bottom to top (oldest to newest), excluding
        frames that are already done.

        Returns
        -------
        List of rule_id integers for open (incomplete) frames.
        """
        return [f.rule_id for f in self._stack if not f.is_done()]

    def compression_trigger(self) -> bool:
        """Return True when the top frame is ready for compression.

        A compression is triggered when the top frame has been fully expanded
        — all symbols in its body have been visited — and the resulting
        sub-tree can be folded back into its parent frame.

        Returns
        -------
        True if the spine is non-empty and the top frame is_done().
        """
        top = self.peek()
        return top is not None and top.is_done()

    def __repr__(self) -> str:
        frames = [f"Rule({f.rule_id}@{f.pos}/{len(f.body)})" for f in self._stack]
        return f"Spine([{', '.join(frames)}])"
